<?php 
namespace common\components; 
use Yii; 
use yii\base\Object;


class ScreeningFormQuestion extends Object
{

	private $_data; 
	
	
	/* ******************************************************************************************************* */ 
    /* ******************************************************************************************************* */ 
    /* ******************************************************************************************************* */ 
    private  function _getData($screening_form_id)
    {
    		return (new \yii\db\Query())
    				->select([
                        's.id as screening_form_question_id' , 's.title as title', 
                        's.name as name' , 's.input_type_id',
                        's.screening_form_id'])
    				->from('screening_form_questions s')
                    ->where('s.status_id=:status_active AND s.screening_form_id=:screening_form_id')
                    ->orderBy('s.sort_order')
    				->addParams([':status_active'=>Types::$status['active']['id'], 
    							 ':screening_form_id'=>$screening_form_id 
    					])
    				->all();

         

    }
    /* ******************************************************************************************************* */ 
    /* ******************************************************************************************************* */ 
    /* ******************************************************************************************************* */ 
    /* ******************************************************************************************************* */ 
    /* ******************************************************************************************************* */ 
    /* ******************************************************************************************************* */ 
    public function getQuestions($screening_form_id)
	{
        
        return $this->_getData($screening_form_id); 

	}
	/* ******************************************************************************************************* */ 
    /* ******************************************************************************************************* */ 
    

}